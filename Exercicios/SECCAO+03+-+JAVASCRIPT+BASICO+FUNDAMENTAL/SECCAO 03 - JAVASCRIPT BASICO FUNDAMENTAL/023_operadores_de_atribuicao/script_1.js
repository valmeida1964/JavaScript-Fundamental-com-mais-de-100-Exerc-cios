// Os operadores de atribuição são usados para atribuir valores a variáveis. 
// Eles podem ser usados para atribuir valores simples ou realizar operações matemáticas
// e atribuir o resultado a uma variável. Os operadores de atribuição mais comuns são:

// O operador de atribuição é o símbolo igual (=). Ele atribui o valor à variável à esquerda do operador.
let num = 10;

// operator de atribuição com adição.
num += 2;       // o operador += adiciona 2 unidades à variável num
// como se dissesse: num = num + 2

// operator de atribuição com subtração
num -= 2;       // o operador -= subtrai 2 unidades da variável num
// como se dissesse: num = num - 2

// operator de atribuição com multiplicação
num *= 2;       // o operador *= multiplica a variável num por 2
// como se dissesse: num = num * 2

// operator de atribuição com divisão
num /= 2;       // o operador /= divide a variável num por 2
// como se dissesse: num = num / 2

// operador de atribuição com exponenciação
num **= 2;      // o operador **= eleva a variável num à potência de 2
// como se dissesse: num = num ** 2

// operador de atribuição com módulo
num %= 2;       // o operador %= calcula o resto da divisão da variável num por 2
// como se dissesse: num = num % 2
