// O que é um bloco de código?
// Um bloco de código é um conjunto de instruções agrupadas entre chaves {}.
// Ele pode ser usado em várias estruturas de controle, como loops, funções e condicionais.
// Os blocos de código são importantes para organizar o código e definir o escopo das variáveis.

// uma instrução if com um bloco de código
if (true) {
    console.log('This is true');
}

// um loop for com um bloco de código
for (let i = 0; i < 10; i++) {
    console.log(i);
}

// uma função com um bloco de código
function myFunction() {
    console.log('This is a function');
}

// um objeto com um bloco de código
let myObject = {
    name: 'John',
    age: 30,
    sayHi: function() {
        console.log('Hi!');
    }
};

// uma classe com um bloco de código
class MyClass {
    constructor() {
        console.log('This is a class');
    }
}

// uma instrução try...catch com um bloco de código
try {
    console.log('This is a try block');
} catch (error) {
    console.log('This is a catch block');
}

