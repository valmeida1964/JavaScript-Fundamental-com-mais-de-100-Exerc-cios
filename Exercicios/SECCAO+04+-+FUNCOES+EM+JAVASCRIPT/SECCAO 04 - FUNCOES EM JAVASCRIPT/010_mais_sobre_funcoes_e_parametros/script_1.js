// O parâmetro de uma função é a variável que recebe o valor quando a função é chamada
// O argumento de uma função é o valor que é passado quando a função é chamada

let a = 5;
let b = 10;

function sum(a, b) {
    return a + b;
}

let result = sum(a, b);

// Quando chamamos a função, os valores das variáveis a e b são passados como argumentos
// Os valores dos argumentos são atribuídos aos parâmetros da função: a e b
// E depois a função é executada